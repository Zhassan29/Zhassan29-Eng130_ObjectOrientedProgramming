def multiply(value1, value2): # take user input as int then  multiply them together display the outcome
    print("this function is to provide multiplication functionality")
    return value1 * value2
print(multiply(5, 5))

def modulus(value1, value2): # take user input as int then together display the outcome
    print("this function is to provide the remainder")
    return value1 % value2
print(modulus(12, 5))

def divide(value1, value2): # take user input as int then  divide them and display the outcome
    print("this function is to provide division functionality")
    return value1 / value2
print(divide(100, 5))